def over(ar,br,n):
    h = [0]*n
    for i in range(n):
        h[i] = br[ar[i]-1]
    return h

def inv(n,ar):
    ans = 0
    for i in range(n-1):
        for j in range(i+1,n):
            if ar[i] > ar[j]: ans += 1
    return ans

